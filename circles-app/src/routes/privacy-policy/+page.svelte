    <p><strong>2. Your Information and the blockchain</strong></p>
    <p>
      Blockchain technology, also known as distributed ledger technology (or
      simply ‘DLT’), is at the core of our business. Blockchains are
      decentralized and made up of digitally recorded data in a chain of
      packages called ‘blocks’. The manner in which these blocks are linked is
      chronological, meaning that the data is very difficult to alter once
      recorded. Since the ledger may be distributed all over the world (across
      several ‘nodes’ which usually replicate the ledger) this means there is no
      single person making decisions or otherwise administering the system (such
      as an operator of a cloud computing system), and that there is no
      centralized place where it is located either.
    </p>
    <p>
      When you register a Circles Profile or interact with the Secret Garden
      App, certain information you provide—such as your username, profile
      picture, location, and any other optional metadata—will be uploaded to the
      InterPlanetary File System (IPFS) and/or recorded on the blockchain. This
      means such information will be publicly accessible and cannot be deleted
      or modified by us or any other party.
    </p>
    <p>
      Blockchain records are ‘immutable’ by design, meaning they cannot be
      changed or deleted. Immutability can impact users’ ability to exercise
      rights such as rights to erasure (‘right to be forgotten’) or to object or
      restrict processing of personal data. Data on the blockchain cannot be
      erased and cannot be changed. Although smart contracts may be used to
      revoke certain access rights, and some content may be made invisible to
      others, it is not deleted.
    </p>
    <p>
      In certain circumstances, in order to comply with our contractual
      obligations to you (such as delivery of tokens), it will be necessary to
      write certain personal data, such as your Ethereum or other cryptocurrency
      wallet address, onto the blockchain; this is done through a smart contract
      and requires you to execute such transactions using your wallet’s private
      key.
    </p>
    <p>
      In most cases, the ultimate decisions to (i) transact on the blockchain
      using your Ethereum or other cryptocurrency wallet address, as well as
      (ii) share the public key relating to your Ethereum or other
      cryptocurrency wallet address with anyone (including us) rests with you.
    </p>
    <p>
      IF YOU WANT TO ENSURE YOUR PRIVACY RIGHTS ARE NOT AFFECTED IN ANY WAY, YOU
      SHOULD NOT TRANSACT ON BLOCKCHAINS AS CERTAIN RIGHTS MAY NOT BE FULLY
      AVAILABLE OR EXERCISABLE BY YOU OR US DUE TO THE TECHNOLOGICAL
      INFRASTRUCTURE OF THE BLOCKCHAIN. IN PARTICULAR, THE BLOCKCHAIN IS
      AVAILABLE TO THE PUBLIC AND ANY PERSONAL DATA SHARED ON THE BLOCKCHAIN
      WILL BECOME PUBLICLY AVAILABLE.
    </p>
    <p>
      <strong
        >WE STRONGLY DISCOURAGE UPLOADING ANY PERSONAL DATA OR INFORMATION TO
